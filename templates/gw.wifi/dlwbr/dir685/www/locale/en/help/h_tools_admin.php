<strong>Helpful Hints..</strong>
<br><br>
&nbsp;&#149;&nbsp; For security reasons, it is recommended that you change the Login Name 
and Password for the Administrator accounts. Be sure to write down the new Login Names 
and Passwords to avoid having to reset the router in the event that they are forgotten.
<br><br>
&nbsp;&#149;&nbsp; When enabling Remote Management, you can specify the IP address of the computer
on the Internet that you want to have access to your router.
<br><br>
&nbsp;&#149;&nbsp; When IP Permitted to Access is 0.0.0.0 which means any IP address from the Internet 
can access the router remotely once remote management is enabled.
