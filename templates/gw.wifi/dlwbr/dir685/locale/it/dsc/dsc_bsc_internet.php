<h1>Configurazione Internet</h1>
Se si sta configurando il dispositivo per la prima volta, si consiglia 
di fare clic sul pulsante Configurazione guidata connessione 
Internet e di seguire le istruzioni visualizzate. 
Per modificare o configurare manualmente le impostazioni del dispositivo, 
fare clic sul pulsante Configurazione manuale connessione Internet.
