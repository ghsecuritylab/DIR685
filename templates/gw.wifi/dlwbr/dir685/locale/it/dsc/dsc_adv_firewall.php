<h1>IMPOSTAZIONI FIREWALL</h1>
<p>
È possibile utilizzare le regole firewall per consentire o impedire il traffico trasmesso attraverso il router. 
È possibile specificare una singola porta utilizzando la casella di immissione nella parte superiore oppure 
un intervallo di porte utilizzando entrambe le caselle di immissione.
</p>
<p>
DMZ è l'acronimo di "Demilitarised Zone".
La funzione DMZ rende accessibili via Internet i computer mascherati dal firewall del router.
Generalmente il server DMZ ospita server Web, FTP e altro ancora.
</p>
