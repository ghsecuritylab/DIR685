<strong>Suggerimenti utili</strong>
<br><br>
&nbsp;&#149;&nbsp; Utilizzare il menu a discesa associato al campo <strong>Nome applicazione</strong> 
per accedere a un elenco di applicazioni predefinite. Se si seleziona 
una delle applicazioni predefinite, fare clic sul pulsante freccia accanto 
al menu a discesa per compilare i campi appropriati.
<br><br>
&nbsp;&#149;&nbsp; È possibile selezionare il computer dall'elenco di client DHCP nel 
menu a discesa <strong>Nome computer</strong> oppure immettere manualmente 
l'indirizzo del computer per il quale aprire la porta specificata.
<br><br>
&nbsp;&#149;&nbsp; Questa funzionalità consente di aprire un intervallo di porte per 
un computer della rete. Per effettuare questa operazione, immettere la prima porta dell'intervallo di porte da aprire nel router 
nella prima casella sotto <strong>Porta pubblica</strong> e l'ultima porta dell'intervallo nella seconda casella. Immettere quindi la prima porta 
dell'intervallo di porte utilizzato dal server interno 
nella prima casella sotto <strong>Porta privata</strong> e l'ultima porta 
dell'intervallo nella seconda casella.
<br><br>
&nbsp;&#149;&nbsp; Per aprire un'unica porta tramite questa funzionalità, immettere
 lo stesso numero in entrambe le caselle. 

