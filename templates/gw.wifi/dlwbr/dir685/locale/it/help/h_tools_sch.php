<strong>Suggerimenti utili</strong><br>
<br>
&nbsp;&#149;&nbsp; Le pianificazioni vengono utilizzate con diverse altre funzionalità per specificare quando tali funzionalità dovranno essere attivate.<br><br>
&nbsp;&#149;&nbsp; Assegnare a ciascuna pianificazione un nome evocativo. Ad esempio, il nome più adatto per una pianificazione relativa a un'operazione che deve essere eseguita dal lunedì al venerdì tra le 15 e le 21 potrebbe 
essere &quot;Dopo scuola&quot;.<br><br>
&nbsp;&#149;&nbsp; Fare clic su <strong>Salva</strong> per aggiungere una pianificazione completata all'elenco seguente.<br><br>
&nbsp;&#149;&nbsp; Fare clic su <strong>Modifica</strong> per modificare una pianificazione esistente.<br><br>
&nbsp;&#149;&nbsp; Fare clic su <strong>Elimina</strong> per eliminare definitivamente una pianificazione.

