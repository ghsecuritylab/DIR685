&nbsp;&#149;&nbsp; 点选 <strong>Application Name</strong> 下拉菜单，您可以从菜单的列表中选择预先定义的应用程序，点击下拉菜单旁边的方向键可填写适当的字段。

<br><br>
&nbsp;&#149;&nbsp; 您可以在<strong>Computer Name</strong>下拉菜单的DHCP客户端列表中选择您的计算机，或是手动输入您需要开启指定端口计算机的IP地址。

<br><br>
&nbsp;&#149;&nbsp; 您可以通过该功能开启网络中一台计算机上的一个端口范围。
如要进行此操作，在<strong>Public Port</strong> 栏中输入路由器所需开启端口范围的起始端口号，
然后在下一栏中输入终止端口号。随后，在<strong>Private Port</strong> 栏中输入内部服务器所需开启端口范围的起始端口号，
然后在下一栏中输入终止端口号。

<br><br>
&nbsp;&#149;&nbsp; 如要开启一个单独的端口来使用该功能，只需在两个端口栏中简单地输入相同的编号即可。
 

