&nbsp;&#149;&nbsp; 计划是由一组已经生效的功能来定义。
<br><br>
&nbsp;&#149;&nbsp; 建议您给每个计划设定一个名称。例如，一个从周一到周五，每天下午3:00到9:00运行的计划，可以被叫做&quot;放学后&quot;。
<br><br>
&nbsp;&#149;&nbsp; 点击<strong>Save</strong>，将一个完成的计划添加到下面的列表中。
<br><br>
&nbsp;&#149;&nbsp; 点击<strong>Edit</strong>图标，更改一个已有的计划。
<br><br>
&nbsp;&#149;&nbsp; 点击<strong>Delete</strong>图标，永久性删除一个计划。


