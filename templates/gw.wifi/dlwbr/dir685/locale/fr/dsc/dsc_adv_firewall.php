<h1>PARAMÈTRES DU PARE-FEU</h1>
<p>
Les règles du pare-feu peuvent servir à autoriser ou refuser le passage du trafic par le routeur. 
Vous pouvez indiquer un seul port en utilisant la boîte de saisie, 
située en haut, ou une plage de ports en utilisant les deux boîtes de saisie.
</p>
<p>
DMZ vient de l'anglais « Demilitarised Zone » et signifie zone démilitarisée.
La DMZ permet aux ordinateurs situés derrière le routeur d'être accessible au trafic Internet.
Généralement, la DMZ contient les serveurs Web, FTP, etc.
</p>
