<h1>Règles de filtrage des sites Web</h1>
<p>
Les Website Filters (Filtres de sites Web) sont des outils utiles pour limiter l'accès à Internet. L'option Website URL (URL des sites Web) 
vous permet de créer rapidement une liste de tous les sites Web pour lesquels vous souhaitez autoriser ou refuser l'accès aux 
utilisateurs. L'option Schedule (Calendrier) vous permet de préciser à quel moment les clients ou 
PC connectés au routeur sont autorisés à accéder à Internet.
</p>
