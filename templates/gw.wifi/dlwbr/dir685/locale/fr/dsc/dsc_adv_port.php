<h1>Règles de redirection de port</h1>
L'option Port Forwarding (Redirection de port) vous permet de définir un seul port public sur votre routeur, 
qui sera redirigé vers une adresse IP interne et un port privé du réseau local, si nécessaire. 
Cette option est utile pour héberger les services en ligne, comme les serveurs FTP ou Web.
