<h1>Règles d'application</h1>
L'option Application Rules (Règles d'application) permet d'ouvrir un ou plusieurs ports de votre pare-feu l
Lorsque le routeur détecte des données envoyées vers Internet sur un port ou une plage de ports "Déclenchement" sortant.
Les règles relatives aux applications spéciales s'appliquent à tous les ordinateurs de votre réseau interne.
