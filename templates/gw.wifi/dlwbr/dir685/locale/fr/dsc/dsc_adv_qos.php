<h1>Moteur QoS (Qualité de service)</h1>
<p>
Cette section permet de configurer Smart QoS de D-Link. Smart QoS améliore la qualité du système de voix sur IP ou 
la diffusion en garantissant que le trafic de voix sur IP ou de diffusion a la priorité sur le reste du trafic réseau (par ex. FTP ou Web). 
Pour optimiser les performances, cochez l'option « lag eliminated » (décalage éliminé) 
qui vous permet de définir la priorité de vos applications automatiquement.
</p>
