<strong>Conseils utiles.</strong>
<br><br>
&nbsp;&#149;&nbsp;Pour des raisons de sécurité, il est recommandé de modifier 
le nom de connexion et le mot de passe des comptes administrateur. Veillez à écrire les nouveaux noms 
d'utilisateur et mots de passe pour éviter de devoir réinitialiser le routeur en cas d'oubli.
<br><br>
&nbsp;&#149;&nbsp; Lorsque vous activez Remote Management (Gestion à distance), 
vous pouvez indiquer l'adresse IP de l'ordinateur sur Internet dont vous souhaitez qu'il ait accès à votre 
réseau ou laisser le champ vierge pour qu'il puisse accéder à n'importe quel ordinateur sur Internet.
<br><br>
&nbsp;&#149;&nbsp; Lorsque IP Permitted to Access (IP autorisée à accéder) est 0.0.0.0, n'importe quelle adresse IP provenant 
d'Internet peut accéder au routeur à distance une fois la gestion à distance activée.
