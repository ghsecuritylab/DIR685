&nbsp;&#149;&nbsp; <strong>DMZ :</strong><br>
Activez l'option DMZ en dernier recours uniquement. Si vous avez des difficultés à utiliser 
une application à partir d'un ordinateur derrière le routeur, commencez par ouvrir les ports associés 
à cette application dans la section Redirection de port avancée.
<br><br>
&nbsp;&#149;&nbsp; <strong>Parefeu :</strong><br>
Les règles du pare-feu constituent une fonction avancée servant à refuser ou autoriser le passage du trafic par le périphérique. 
Vous pouvez créer des règles détaillées pour le périphérique.
Veuillez vous reporter au manuel pour de plus amples détails et exemples.
