<strong>Sugerencias útiles..</strong>
<br><br>
&nbsp;&#149;&nbsp; Compruebe el menú desplegable <strong>Nombre de la aplicación</strong> para ver una lista de aplicaciones 
predefinidas de la que puede hacer su selección. Si selecciona una de las aplicaciones predefinidas, 
haga clic en el botón de flecha situada junto al menú desplegable 
para rellenar los campos adecuados.
<br><br>
&nbsp;&#149;&nbsp; Puede seleccionar su ordenador de la lista de clientes DHCP en 
el menú desplegable <strong>Nombre de ordenador</strong>, o introducir manualmente 
la dirección IP del ordenador al que desea abrir el puerto especificado.
<br><br>
&nbsp;&#149;&nbsp; Esta característica permite abrir un rango de puertos a un ordenador de la red. 
Para hacerlo, introduzca en el primer cuadro el primer puerto del rango que le gustaría abrir en el router, bajo 
<strong>Puerto público</strong>, y el último puerto del rango en el segundo cuadro. Después de que 
haya introducido en el primer cuadro el primer puerto del rango que el servidor interno utiliza, 
bajo <strong>Puerto privado</strong>, y el último puerto 
del rango en el segundo cuadro.
<br><br>
&nbsp;&#149;&nbsp; Para abrir un único puerto utilizando esta funcionalidad, introduzca 
simplemente el mismo número en ambos cuadros. 

