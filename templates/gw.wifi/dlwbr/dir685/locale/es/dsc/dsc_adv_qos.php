<h1>Motor QoS (Calidad de servicio)</h1>
<p>
Use esta sección para configurar Smart QoS de D-Link. Smart QoS mejora la calidad o el streaming de VoIP 
asegurando la priorización de su tráfico de VoIP o de streaming con respecto a otro tráfico de red, como por ejemplo FTP o Web. 
Para lograr el rendimiento óptimo, marque la opción "lag eliminated" para establecer 
automáticamente la prioridad de sus aplicaciones.
</p>
