<h1>Reglas del filtro del sitio web </h1>
<p>
Los Filtros del sitio web son herramientas útiles para restringir el acceso a Internet. La opción URL del sitio web le permite crear rápidamente 
una lista de sitios web a los que desea permitir o impedir que accedan los usuarios. 
La opción Programa le permitirá controlar cuándo se le permite el acceso 
a Internet a los clientes o PCs conectados al Router.
</p>
