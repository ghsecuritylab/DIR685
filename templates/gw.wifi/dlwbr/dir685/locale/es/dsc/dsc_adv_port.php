<h1>Reglas de direccionamiento de puerto</h1>
La opción Direccionamiento de puerto le permite definir un único puerto público en su router para la redirección 
a una Dirección IP de LAN interna y a un puerto LAN privado si fuera necesario. 
Esta funcionalidad es útil para alojar servicios on line como Servidores FTP o Web.
