<h1>Reglas de la aplicación</h1>
La opción Reglas de la aplicación se utiliza para abrir puertos sencillos o múltiples en su cortafuegos cuando 
el router percibe el envío de datos a Internet en un puerto o rango de puertos de "Activación" de salida.
Las reglas de aplicaciones especiales se aplican a todos los ordenadores de su red interna.
