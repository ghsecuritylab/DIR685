<a name="disk_management"><h2>ディスク管理</h2></a>
	<table border=0 cellspacing=0 cellpadding=0>
<tr>
	<td>
		<p>
			ディスク管理ページでは、デバイス内の現在のローカルハードディスクの情報表示および設定を行います。
			<br>
			<br>
			<strong><em>ハードディスク情報</em></strong> -
			デバイス内のハードディスクの詳細情報を表示します。<br>
			<strong><em>フォーマット</em></strong> -
			このボタンをクリックすると、デバイス内のハードディスクのフォーマットを実行します。<br>
			警告：この操作により、ハードディスクが初期化されます。ハードディスク内のすべてのデータが失われるため注意してご利用ください。
			 <br>
			<strong><em>ハードディスク設定</em></strong> -
			この機能を有効化すると、指定時間（分）の間ハードディスクにデータの読み書きが行われなかった場合、ハードディスクはスタンバイモードに移行して、電力消費を抑えます。<br>
		</p>
	</td>
</tr>
 </table>
</div>

