<a name="storage_access"><h2>ストレージアクセス</h2></a>
<table border=0 cellspacing=0 cellpadding=0>
 <tr>
 <td height=64> 
		<p>
			<strong><em>カテゴリ</em></strong> -
			ユーザまたはグループを選択します。<br>
			<strong><em>ユーザまたはグループ</em></strong> -
			プルダウンリストからユーザまたはグループを選択します。すべてのユーザを対象にしたルールを作成したい場合は、「全アカウント」を選択します。<br>
			<strong><em>フォルダ</em></strong> -
			「参照」ボタンをクリックしてフォルダを選択します。内部ドライブ上のフォルダを指定する必要があります。<br>
			<strong><em>アクセス権</em></strong> -
			指定したユーザまたはグループに対して選択したフォルダへのアクセス権限を「読み取り専用」または「読み書き」から選択します。<br>
			<strong><em>Oplocks</em></strong> -
			Oplock（Opportunistic lock：便宜的ロック）は、32ビット版のWindows OS環境に実装されているLANマネージャネットワークプロトコルによる機能です。Oplockはサーバがクライアントに対して共有論理ボリュームを保証しておくことです。この保証により、サーバがファイルの内容を変更できないことがクライアントに通知されます。または、ファイルが変更されそうになると、実際に変更が許可される前に、クライアントに通知されます。Oplockはネットワークでのファイル共有において、パフォーマンスを向上させる目的で設計されました。しかし、ファイルベースのデータベースアプリケーションを使用する際には、Oplock機能を<strong>いいえ（オフ）</strong>にすることをお勧めします。
			<br>
			<br>
			Oplockにより影響を受けるOSとは？<br>
			<br>
			Windows Vistaでは、Oplockはデフォルトで有効になっており、無効化はできません。DIR-685において、ファイルを共有するアプリケーションの使用時にネットワークパフォーマンスに問題がある場合、Oplock機能を<strong>いいえ（オフ）</strong>にして改善できる場合があります。
			<br>
			<strong><em>マップアーカイブ</em></strong> -
			この機能を有効にすると、ファイルがDIR-685に保存される際に、ファイル属性「アーカイブ」がコピーされます。ある種のバックアップソフトウェアは、バックアップとして保存されるファイルにこの属性を付けます。<br>
			<strong><em>コメント</em></strong> -
			ルールに対する追加コメント。入力は任意で、機能に影響は与えません。<br>
			<br>
			「設定を保存する」をクリックしてルールを保存します。保存されたルールは、本ページの下部にリスト表示されます。ルールの編集/削除は、ルール右側の「ノートと鉛筆」アイコンや「ゴミ箱」アイコンをクリックして行えます。
			<br>
		</p>
 </tr>
</table>
</div>
 
 
