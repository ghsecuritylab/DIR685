<a name="users_groups"><h2>ユーザ/グループ</h2></a>
	<table border=0 cellspacing=0 cellpadding=0>
<tr>
	<td height=228>
		<p>
			<strong>ユーザ</strong><br>
			ここではユーザの登録を行います。登録されたユーザには、ネットワークアクセスやFTPサーバのセクションで、ルールの設定を行えます。また、時間指定ダウンロードのセクションにもアクセスできます。
		</p>
		<p>
			<strong><em>ユーザ名</em></strong> -
			ユーザ名を入力します。<br>
			<strong><em>パスワード</em></strong> -
			パスワードを入力します。<br>
			<strong><em>確認用パスワード</em></strong> -
			確認のためもう一度パスワードを入力します。「設定を保存する」をクリックすると、ユーザが追加されます。<br>
			<br>
			登録されたユーザは、このページの下部にリスト表示されます。ユーザがグループに割り当てられていればグループ名も一緒に表示されます。ユーザパスワードの変更は、「ノートと鉛筆」アイコンをクリックして行えます。また、ルールの右側の「ゴミ箱」アイコンをクリックするとユーザを削除できます。
		</p>
		<p>
			<strong>グループ</strong><br>
			ここではグループの登録を行います。登録されたグループは、「ユーザ」ページ上の「グループ」に表示され、選択可能になります。
			<br>
			<br>
			登録されたグループは、このページの下部にリスト表示されます。このグループに属するユーザも一緒に表示されます。
			<br>
		</p>
	</td>
</tr>
 </table>
