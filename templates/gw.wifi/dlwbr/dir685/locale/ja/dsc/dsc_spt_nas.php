<a name="ftp_server"><h2>FTPサーバ</h2></a>
<table border=0 cellspacing=0 cellpadding=0>
<tr>
	<td height=99>
		<p>
			<strong><em>カテゴリ</em></strong> -
			ユーザまたはグループを選択します。<br>
			<strong><em>ユーザ</em></strong> -
			プルダウンリストからユーザまたはグループを選択します。全ユーザに適用するルールを作成する場合は、「全アカウント」を選択します。「匿名ユーザ」は常にユーザリストに存在します。<br>
			<strong><em>フォルダ</em></strong> -
			「参照」をクリックしてフォルダを指定し、このフォルダを選択したユーザに割り当てます。内部ディスク上に存在するフォルダを指定する必要があります。すべてのディスクのルートディレクトリへのアクセス権を同時に割り当てたい場合は、「ルート」ボックスを選択します。<br>
			<strong><em>アクセス権</em></strong> -
			選択したユーザまたはグループから、選択したフォルダへのアクセス権を「読み取りのみ」または「読み書き」から指定します。「追加」ボタンをクリックすると、リストにルールを追加できます。保存されたルールは、本ページの下部にリスト表示されます。ルールの編集/削除は、ルール右側の「ノートと鉛筆」アイコンや「ゴミ箱」アイコンをクリックして行えます。<br>
			<strong><em>最大ユーザ数</em></strong> -
			一度にFTPサーバに接続できるユーザの最大数を指定します。デフォルトでは10が設定されています。<br>
			<strong><em>アイドル時間</em></strong> -
			この時間以上操作を行わなかったユーザはログアウトされます。<br>
			<strong><em>ポート</em></strong> -
			FTPサーバが使用するポートを変更したい場合は、こちらに新しい番号を指定します。本設定の変更は上級者向きであり、変更によってルータの背後での使用に問題が生じる場合があります。FTPポートの変更を行った経験のある方以外は、デフォルト値（21）のままご使用になることをお勧めします。<br>
			<strong><em>フローコントロール</em></strong> -
			FTPサーバを通るデータフローを調整します。この機能を使用して制限を設けると、FTPサーバからのダウンロード速度が遅くなる場合がありますが、ローカルの帯域を抑えることができます。<br>
			<strong><em>クライアント言語</em></strong> -
			Windows FTPのような標準的なFTPクライアントでは、多くの場合ファイル転送時のコードページとして西ヨーロッパ言語しかサポートしていません。北ヨーロッパ言語、中央ヨーロッパ言語、繁体字/簡体字中国語、およびキリル言語を追加して、これらのコードページ対応の非標準的なFTPクライアントが使用できるように致しました。<br>
			<br>
			設定が完了したら「設定を保存する」をクリックします。
		</p>
	</td>
</tr>
</table>
</div>


