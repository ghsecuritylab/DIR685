<a name="bt_download_manager"><h2>BTダウンロードマネージャ</h2></a>
	<table border=0 cellspacing=0 cellpadding=0>
<tr>
	<td>
		<p>
			「BTダウンロードマネージャ」ページではPCの電源を投入することなくBitTorrentファイルをインターネット上からダウンロードするための設定を行います。
			<br>
			<br>
			<strong><em>BT設定</em></strong> -
			BTダウンロードマネージャサービスをオン/オフします。「ディレクトリパス」には、ダウンロードしたファイルの保存先となるディレクトリ/フォルダを指定します。ダウンロードしたすべてのファイルは指定したディレクトリに保存されます。ダウンロードが完了したファイルは「completed」フォルダに、ダウンロード中のファイルは「incomplete」フォルダに格納されます。オリジナルファイルと同様のファイル構成で保存されます。<br>
			<strong><em>Torrentダウンロード</em></strong> -
			「BTダウンロードマネージャ」ページでは、あるURLまたはBTシードファイルからダウンロードアイテムを追加するかを選択します。<br>
			ダウンロード中のすべてのアイテムは、ダウンロードごとにウィンドウ下部にリスト表示され、確認および制御が可能です。
			<br>
			<strong><em>ダウンロード中のアイテムリスト</em></strong> -
			ダウンロード中の各アイテムの詳細は「詳細」リンクをクリックして参照できます。<br>
			<strong><em>停止</em></strong> -
			このボタンをクリックしてダウンロードプロセスを停止/一時停止して、ダウンロード中の他のアイテムのために帯域を空けることができます。<br>
			<strong><em>削除</em></strong> -
			ダウンロードエラーがあった場合には、各アイテムの右側のボタンを使用して、アイテムのダウンロードをキャンセル/削除できます。<br>
			<br>
			削除対象のアイテムのダウンロードが既に完了している場合、エントリのみが削除され、ダウンロード済みのファイルは"completed"ディレクトリ内に保存されます。
			<br>
			<strong><em>削除済み項目</em></strong> -
			ダウンロード中のアイテムリストの右上にあるボタンをクリックすると、ダウンロードが完了した全てのアイテムをリストから削除できます。このときダウンロードしたファイル自体が削除されるわけではありません。<br>			
		</p>
	</td>
</tr>
 </table>
