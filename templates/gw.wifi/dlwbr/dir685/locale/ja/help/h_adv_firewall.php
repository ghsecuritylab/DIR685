&nbsp;&#149;&nbsp; <strong>DMZ：</strong><br>
DMZ機能は最後の手段として利用してください。ルータの背後に配置されたコンピュータがアプリケーションを正常に利用できない場合、まず「詳細設定」の「ポートフォワーディング」セクションでそのアプリケーションで使用するポートを開放して解決を試みてください。


<br><br>
&nbsp;&#149;&nbsp; <strong>ファイアウォール：</strong><br>
ファイアウォールルールは、デバイスを通過するトラフィックを許可または禁止する
拡張機能です。デバイスに対して詳細なルールを作成できます。
機能の詳細や設定例については、マニュアルをご参照ください。
