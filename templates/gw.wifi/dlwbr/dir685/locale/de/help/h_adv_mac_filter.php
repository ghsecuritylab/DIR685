<strong>Hilfreiche Tipps...</strong>
<br><br>
&nbsp;&#149;&nbsp; Erstellen Sie eine Liste mit MAC-Adressen und wählen Sie, ob ihnen der Zugriff auf Ihr Netzwerk gestattet oder verweigert werden soll.
<br><br>
&nbsp;&#149;&nbsp;Computer, die eine IP-Adresse vom DHCP-Server des Routers erhalten haben, erscheinen in der DHCP-Client-Liste. Wählen Sie ein 
Gerät aus dem Dropdown-Menü aus und klicken Sie auf den Pfeil, um die MAC-Adresse dieses Geräts in die Liste aufzunehmen.
<br><br>
&nbsp;&#149;&nbsp; Verwenden Sie das Kontrollkästchen auf der linken Seite, um einen bestimmten Eintrag zu aktivieren oder zu deaktivieren.
<br><br>
&nbsp;&#149;&nbsp; Verwenden Sie das Dropdown-Menü <strong>Immer</strong>, wenn Sie vorher bereits einen Zeitplan auf dem Router festgelegt haben.
Ist das noch nicht geschehen, klicken Sie auf die Schaltfläche <strong>Add New</strong> (Neu hinzufügen), um einen Zeitplan hinzuzufügen.
