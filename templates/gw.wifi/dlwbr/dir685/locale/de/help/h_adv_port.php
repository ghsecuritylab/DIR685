<strong>Hilfreiche Tipps...</strong>
<br><br>
&nbsp;&#149;&nbsp; Im Dropdown-Menü <strong>Anwendungsname</strong> 
finden Sie eine Liste vordefinierter Anwendungen, die Sie auswählen können. 
Wenn Sie eine der vordefinierten Anwendungen auswählen, klicken Sie auf die Pfeilschaltfläche 
neben dem Dropdown-Menü, um die entsprechenden Felder auszufüllen.
<br><br>
&nbsp;&#149;&nbsp; Sie können Ihren Computer aus der Liste von DHCP-Clients im Dropdown-Menü 
<strong>Computername</strong> auswählen oder manuell die IP-Adresse des Computers 
eingeben, für den Sie den angegebenen Port öffnen möchten.
<br><br>
&nbsp;&#149;&nbsp; Mit dieser Funktion können Sie einen Bereich mit Ports 
für einen Computer in Ihrem Netzwerk öffnen. Geben Sie zu diesem Zweck im ersten Feld unter 
<strong>Öffentlicher Port</strong> den ersten Port in dem Bereich ein, den Sie öffnen möchten, und den letzten Port des Bereichs 
in dem zweiten Feld. Geben Sie dann den ersten Port in dem Bereich, den der interne Server verwendet, 
in dem ersten Feld unter <strong>Privater Port</strong> und den letzten 
Port des Bereichs in dem zweiten Feld ein.
<br><br>
&nbsp;&#149;&nbsp; Um in dieser Funktion einen einzigen Port zu öffnen, geben Sie einfach die 
gleiche Portnummer in beiden Feldern ein. 

