&nbsp;&#149;&nbsp; <strong>DMZ:</strong><br>
Aktivieren Sie die DMZ-Option nur als letzten Ausweg. Wenn es Ihnen Probleme bereitet, eine Anwendung auf einem 
Computer hinter dem Router zu verwenden, versuchen Sie zunächst, die der Anwendung zugeordneten 
Ports im Teil über die speziellen Portweiterleitungsoptionen zu öffnen.
<br><br>
&nbsp;&#149;&nbsp; <strong>Firewall:</strong><br>
Firewall-Regeln stellen eine erweiterte Funktion dar, mit der Sie den Datenverkehr durch das 
Gerät zulassen oder verweigern können. Dazu können Sie detaillierte Regeln für das Gerät erstellen.
Weitere Informationen und Beispiele dazu finden Sie im Handbuch.
