<strong>Hilfreiche Tipps...</strong>
<br><br>
&nbsp;&#149;&nbsp; Aus Sicherheitsgründen wird empfohlen, den Anmeldenamen und das Kennwort 
für die Administratorkonten zu ändern. Notieren Sie sich die neuen Anmeldenamen und Kennwörter, 
damit Sie den Router nicht zurücksetzen müssen, wenn sie einmal vergessen wurden.
<br><br>
&nbsp;&#149;&nbsp; Bei Aktivierung des Fernmanagements können Sie die IP-Adresse des Computers im Internet angeben, 
dem Zugriff auf Ihren Router eingeräumt werden soll, oder Sie machen keine Angabe, 
um so jedem Computer im Internet den Zugriff zu gestatten.
<br><br>
&nbsp;&#149;&nbsp; Wenn 'IP zugelassen zum Zugriff' 0.0.0.0 ist, bedeutet das, dass der Fernzugriff jeder IP-Adresse vom Internet 
aus auf den Router möglich ist, sobald das Fernmanagement aktiviert ist.
