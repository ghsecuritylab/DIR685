&nbsp;&#149;&nbsp; Die Funktion "Wake on LAN" (WOL) kann dazu genutzt werden, einen LAN-seitig mit dem Router verbundenen PC im Fernzugriff einzuschalten. Geben Sie den UDP-Port an. Er wird vom 'WOL Magic Packet', einem speziellen Datenpaket, verwendet, um zu verhindern, dass der Router die WOL-Anfrage als feindlichen Angriff ansieht und sperrt.<br>

