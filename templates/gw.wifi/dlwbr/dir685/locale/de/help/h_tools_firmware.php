&nbsp;&#149;&nbsp; <strong>Firmware-Aktualisierungen</strong> werden regelmäßig veröffentlicht, um die Funktionalität Ihres Routers 
zu verbessern und Funktionen hinzuzufügen. Sollte ein Problem mit einer spezifischen Funktion des Routers aufgetreten sein, 
sehen Sie auf der 'Support'-Webseite von D-Link nach, um zu prüfen, ob eine aktualisierte Version der Firmware für Ihren Router verfügbar ist. 
Klicken Sie dazu auf <strong>Click here to check for an upgrade on our support site</strong> 
(Klicken Sie hier, um auf unserer Support-Website nach einer Aktualisierung zu suchen).
