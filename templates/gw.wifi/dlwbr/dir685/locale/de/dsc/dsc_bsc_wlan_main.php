<h1>Funk-Setup</h1>
<p>
Es gibt zwei Möglichkeiten, Ihre Funk- oder drahtlose Verbindung einzurichten.
Sie können den entsprechenden Setup-Assistenten für die 
Funkverbindung verwenden oder die Verbindung manuell konfigurieren.
</p>
<p><b>
Bitte beachten Sie, dass in diesem Abschnitt durchgeführte Änderungen auch auf Ihren Funk-Clients und Ihren PCs eingestellt werden müssen.
</b></p>
