<h1>QoS (Quality of Service) Engine</h1>
<p>
Verwenden Sie diesen Teil, um D-Links Smart QoS zu konfigurieren. Smart QoS verbessert die VoIP-Sprachqualität und das Streaming, 
indem sichergestellt wird, dass VoIP- oder Streaming-Datenverkehr gegenüber anderem Datenverkehr im Netz, wie FTP- oder Web-Daten, priorisiert wird. 
Um die beste Leistung zu erzielen, markieren Sie die Option Lag eliminated (Verzögerung eliminieren), 
um die Priorität für Ihre Anwendungen anzugeben.
</p>
