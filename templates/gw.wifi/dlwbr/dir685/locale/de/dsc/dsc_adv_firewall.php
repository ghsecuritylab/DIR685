<h1>FIREWALL-EINSTELLUNGEN</h1>
<p>
Firewall-Regeln werden dazu verwendet, Datenverkehr über den Router zuzulassen oder zu verhindern. 
Sie können eine einzelnen Port angeben, indem Sie oben das Eingabefeld verwenden, 
oder Sie geben einen Portbereich an, indem Sie beide Felder verwenden.
</p>
<p>
DMZ bedeutet Demilitarized Zone/Demilitarisierte Zone.
DMZ ermöglicht, dass Computer hinter der Firewall des Routers für den Internetdatenverkehr unbeschränkt zugänglich sind.
In der DMZ können beispielsweise Webserver, FTP-Server und andere Computer betrieben werden.
</p>
