<h1>Einstellungen für WOL (Wake on LAN)</h1>
<p>
Mithilfe der Option WOL (Wake on LAN) können Sie den UDP-Port auf Ihrem Router festlegen, der zum Empfang so genannter Magic Packets aus dem Internet verwendet wird.
 Mithilfe dieser Funktion wird ein Computer im Fernzugriff durch ein Magic Packet eingeschaltet oder aus dem Schlafmodus geholt.
</p>
