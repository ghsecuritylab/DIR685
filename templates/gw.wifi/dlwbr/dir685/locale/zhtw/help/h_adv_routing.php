<strong>使用小提示...</strong>
<br>
<p>&nbsp;&#149;&nbsp;啟動:<br>
可指定輸入是否為啟用或禁用。</p>

<p>&nbsp;&#149;&nbsp;介面:<br>
可指定介面––WAN 或 WAN 實體––在使用路由器時，
必須使用 IP 封包並傳輸到路由器。
</p>

<p>&nbsp;&#149;&nbsp;目的地 IP:<br>
封包的 IP 位址會採取本路線。</p>

<p>&nbsp;&#149;&nbsp;子網路遮罩:<br>
遮罩的一個位元指定必須符合哪些 IP 位址的位元。
</p>

<p>&nbsp;&#149;&nbsp;閘道器:<br>
假如使用本路線，可指定下一個目標。
閘道器 0.0.0.0 意指沒有下一個目標，且 IP 位址符合直接連接到所指定介面上的路由器：WAN 或 WAN 實體。
</p>

