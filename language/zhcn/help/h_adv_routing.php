<strong>帮助提示..</strong>
<br>
<p>&nbsp;&#149;&nbsp;启用：<br>
指定是否需要启用或禁用条目。
</p>

<p>&nbsp;&#149;&nbsp;接口：<br>
指定接口——WAN接口或物理WAN接口——即在使用该条路由时，IP数据报必须用来发送出路由器的接口。
</p>

<p>&nbsp;&#149;&nbsp;目的IP：<br>
数据报的IP地址将使用该路由。</p>

<p>&nbsp;&#149;&nbsp;子网掩码：<br>
子网掩码中的一个字节指定了IP地址必须匹配哪些字节。
</p>

<p>&nbsp;&#149;&nbsp;网关:<br>
当使用该路由时，指定所需采用的下一跳。地址为0.0.0.0的网关表示没有下一跳，而且匹配的IP地址是直接连接到“WAN接口和物理WAN接口”所指定的路由器。
</p>

