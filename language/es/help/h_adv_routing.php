<strong>Sugerencias útiles..</strong>
<br>
<p>&nbsp;&#149;&nbsp;Activar:<br>
Especifica si la entrada estará activada o desactivada.</p>

<p>&nbsp;&#149;&nbsp;Interfaz:<br>
Especifica la interfaz -- WAN o WAN física -- que el paquete IP debe 
utilizar para salir del router, cuando se utilice este router.</p>

<p>&nbsp;&#149;&nbsp;IP destino:<br>
La dirección IP de paquetes que tomará esta ruta.</p>

<p>&nbsp;&#149;&nbsp;Máscara de subred:<br>
Un bit en la máscara especifica qué bits de la dirección IP deben coincidir.</p>

<p>&nbsp;&#149;&nbsp;Puerta de enlace:<br>
Especifica el siguiente punto de conexión al que se deberá acceder si se utilizara esta ruta. Una puerta de enlace de 0.0.0.0 
implica que no hay un salto siguiente, y que la dirección IP que coincide está directamente 
conectada al router en la interfaz especificada: WAN o WAN física.</p>

