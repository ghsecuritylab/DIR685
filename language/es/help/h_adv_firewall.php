&nbsp;&#149;&nbsp; <strong>DMZ:</strong><br>
Sólo active la opción DMZ como último recurso. Si tiene problemas al utilizar una aplicación desde 
un ordenador detrás del router, intente en primer lugar abrir los puertos asociados 
con la aplicación en la sección Direccionamiento de puerto avanzado.
<br><br>
&nbsp;&#149;&nbsp; <strong>Cortafuegos:</strong><br>
Las Reglas del cortafuegos representan una funcionalidad avanzada utilizada para denegar o permitir 
que el tráfico pase a través del dispositivo. Puede crear reglas detalladas del servicio.
Consulte el manual para disponer de más detalles y ejemplos.
