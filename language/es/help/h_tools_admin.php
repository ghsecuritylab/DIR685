<strong>Sugerencias útiles..</strong>
<br><br>
&nbsp;&#149;&nbsp; Por razones de seguridad, se recomienda cambiar el Nombre de inicio 
de sesión y la Contraseña para las cuentas de administrador. Asegúrese de escribir los nuevos Nombres de inicio de sesión 
y Contraseñas para evitar tener que reiniciar el router en el caso de que las olvide.
<br><br>
&nbsp;&#149;&nbsp; Al activar Gestión remota, puede especificar la dirección IP del ordenador 
en Internet que desea que tenga acceso a su router, o dejarlo en blanco 
para permitir el acceso a cualquier otro ordenador en Internet.
<br><br>
&nbsp;&#149;&nbsp; Cuando IP permitida para el acceso es 0.0.0.0 lo que significa que cualquier 
dirección IP de Internet puede acceder al router remotamente en cuanto se active la gestión remota.
