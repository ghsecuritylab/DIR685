<h1>Configuración de Internet</h1>
Si está configurando por primera vez el dispositivo, recomendamos hacer clic en el botón 
Asistente de configuración de conexión de Internet y seguir 
las instrucciones que aparecen en la pantalla. 
Si deseara modificar o configurar manualmente los ajustes del dispositivo, 
haga clic en el botón Configuración manual de la conexión de Internet.
