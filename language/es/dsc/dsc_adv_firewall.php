<h1>PARÁMETROS DEL CORTAFUEGOS</h1>
<p>
Las reglas del cortafuegos se pueden utilizar para permitir o denegar que pase el tráfico a través del router. 
Puede especificar un único puerto utilizando el cuadro de entrada en la parte superior o 
un rango de puertos utilizando ambos cuadros de entrada.
</p>
<p>
DMZ significa "Zona desmilitarizada".
DMZ permite que los ordenadores que están detrás del cortafuegos del router sean accesibles al tráfico en Internet.
Normalmente, su DMZ contendría servidores web, servidores FTP y otros.
</p>
