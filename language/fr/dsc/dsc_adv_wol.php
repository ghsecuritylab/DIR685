<h1>Paramètres Wake on LAN</h1>
<p>
L'option Wake on LAN (Réveil à distance) vous permet de définir le port UDP de votre routeur qui servira à recevoir les Magic Packets (Paquets magiques) d'Internet.
 La fonction Wake on LAN (Réveil à distance) permet d'allumer ou de rallumer un ordinateur en veille à distance, à l'aide d'un Magic Packet (Paquet magique).
</p>
