<h1>Configuration Internet</h1>
Si vous configurez le périphérique pour la première fois, nous vous conseillons de cliquer sur le bouton 
Assistant de configuration de connexion Internet et de suivre 
les instructions qui s'affichent à l'écran. 
Si vous voulez modifier ou configurer manuellement les paramètres du périphérique, 
cliquez sur le bouton Configuration manuelle de connexion Internet.
