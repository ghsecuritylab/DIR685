<h1>Calendriers</h1>
L'option Schedule Configuration (Configuration de calendriers) sert à gérer les règles de calendrier « Internet Setup » (Configuration Internet), « Wireless Setup » (Configuration sans fil), 
« Network Filter » (Filtre réseau), « Guest Zone » (Zone invité), « Firewall Settings » (Paramètres du pare-feu) et « Website Filter » (Filtre de sites Web).<br><br>
