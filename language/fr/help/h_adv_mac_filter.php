<strong>Conseils utiles.</strong>
<br><br>
&nbsp;&#149;&nbsp; Créez une liste d'adresses MAC et choisissez de leur accorder ou de leur refuser l'accès à votre réseau.
<br><br>
&nbsp;&#149;&nbsp;Les ordinateurs ayant obtenu une adresse IP à partir du serveur DHCP du routeur apparaissent dans la liste de clients DHCP. 
Sélectionnez un périphérique dans le menu déroulant puis cliquez sur la flèche pour ajouter sa MAC dans la liste.
<br><br>
&nbsp;&#149;&nbsp; Utilisez la case à cocher de gauche pour activer ou désactiver une entrée donnée.
<br><br>
&nbsp;&#149;&nbsp; Utilisez le menu déroulant <strong>Always</strong> (Toujours) si vous avez déjà défini un calendrier dans le routeur.
Dans le cas contraire, cliquez sur le bouton  <strong>Add New</strong> (Ajouter un nouveau) pour en ajouter un.
