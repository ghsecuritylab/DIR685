<strong>Conseils utiles.</strong><br>
<br>
&nbsp;&#149;&nbsp; Les calendriers sont utilisés avec de nombreuses autres fonctions pour déterminer quand ces fonctions sont appliquées.<br><br>
&nbsp;&#149;&nbsp; Attribue un nom significatif pour vous à chaque calendrier. Par exemple, un calendrier Lundi à vendredi de 15h00 à 21h00 peut être 
appelé &quot;Après l'école&quot;.<br><br>
&nbsp;&#149;&nbsp; Cliquez sur <strong>Save</strong> (Enregistrer) pour ajouter un calendrier terminé dans la liste au-dessous.<br><br>
&nbsp;&#149;&nbsp; Cliquez sur l'icône  <strong>Edit</strong> (Éditer) pour modifier un calendrier existant.<br><br>
&nbsp;&#149;&nbsp; Cliquez sur l'icône  <strong>Delete</strong> (Supprimer) pour supprimer définitivement un calendrier.

