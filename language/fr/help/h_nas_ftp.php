Si votre DIR-685 est connecté à un routeur, vous devez transmettre le port 21 (dans la configuration du routeur) à l'adresse IP du DIR-685. Dans le monde extérieur, n'oubliez pas d'accéder à ce serveur FTP en utilisant l'adresse du réseau étendu de votre routeur.
