<strong>Conseils utiles.</strong>
<br><br>
&nbsp;&#149;&nbsp; Vérifiez si le menu déroulant <strong>Application Name</strong>  
(Nom d'application) comporte une liste d'applications prédéfinies vous permettant de réaliser votre choix. 
Si vous sélectionnez l'une d'elles, cliquez sur le bouton fléché en regard du menu 
déroulant pour remplir les champs correspondants.
<br><br>
&nbsp;&#149;&nbsp; Vous pouvez sélectionner votre ordinateur dans la liste de clients DHCP du menu 
déroulant <strong>Computer Name</strong> (Nom d'ordinateur) ou saisir manuellement l'adresse 
IP de l'ordinateur sur lequel vous souhaitez ouvrir le port indiqué.
<br><br>
&nbsp;&#149;&nbsp; Cette fonction vous permet d'ouvrir une plage de ports sur un ordinateur de votre réseau. 
Pour cela, saisissez le premier port de la plage que vous souhaitez ouvrir sur le routeur dans le premier champ, 
sous <strong>Public Port</strong> (Port public) et le dernier port de la plage dans le deuxième. 
Ensuite, entrez le premier port de la plage utilisé par le serveur interne dans la première case, 
sous <strong>Private Port</strong> (Port privé) 
et le dernier dans la deuxième.
<br><br>
&nbsp;&#149;&nbsp; Pour ouvrir un seul port à l'aide de cette fonction, il suffit de saisir 
le même numéro dans les deux champs. 

