<strong>Conseils utiles.</strong>
<br>
<p>&nbsp;&#149;&nbsp;Enable (Activer) :<br>
Indique si l'entrée est activée ou désactivée.</p>

<p>&nbsp;&#149;&nbsp;Interface :<br>
Indique l'interface (réseau étendu ou réseau étendu physique) que le paquet IP doit utiliser 
pour sortir du routeur, lorsque cette voie est utilisée.</p>

<p>&nbsp;&#149;&nbsp;Destination IP (IP cible) :<br>
Adresse IP des paquets empruntant cette route.</p>

<p>&nbsp;&#149;&nbsp;Subnet Mask (Masque de sous-réseau) :<br>
Un bit dans le masque qui indique quels bits de l'adresse IP doivent correspondre.</p>

<p>&nbsp;&#149;&nbsp;Gateway (Passerelle) :<br>
Définit le prochain bon à prendre si cette route est utilisée. Une passerelle de 0.0.0.0. 
indique qu'il n'y a pas de bon à prendre et que l'adresse IP correspondante est connectée directement au 
routeur sur l'interface indiquée : réseau étendu ou réseau étendu physique.</p>

