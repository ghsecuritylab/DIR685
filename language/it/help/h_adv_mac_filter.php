<strong>Suggerimenti utili</strong>
<br><br>
&nbsp;&#149;&nbsp; Consente di creare un elenco di indirizzi MAC e scegliere se consentire o negare loro l'accesso alla rete.
<br><br>
&nbsp;&#149;&nbsp; I computer che hanno ottenuto un indirizzo IP dal server DHCP del router saranno inclusi nell'elenco dei client DHCP. 
Selezionare un dispositivo dal menu a discesa, quindi fare clic sulla freccia per aggiungere l'indirizzo MAC del dispositivo all'elenco.
<br><br>
&nbsp;&#149;&nbsp; Utilizzare la casella di controllo a sinistra per abilitare o disabilitare una voce specifica.
<br><br>
&nbsp;&#149;&nbsp; Utilizzare il menu a discesa <strong>Sempre</strong> se è già stata definita una pianificazione nel router.
In caso contrario, fare clic su <strong>Aggiungi Nuova</strong> per aggiungerne una.
