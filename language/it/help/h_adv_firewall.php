<strong>Suggerimenti utili</strong>
<br><br>
&nbsp;&#149;&nbsp; <strong>DMZ:</strong><br>
Abilitare l'opzione DMZ solo in caso di reale necessità. Se si riscontrano problemi durante l'utilizzo 
di un'applicazione da un computer mascherato dal router, provare dapprima ad aprire 
le porte associate all'applicazione nella sezione Inoltro porte avanzato.
<br><br>
&nbsp;&#149;&nbsp; <strong>Firewall:</strong><br>
Le regole firewall sono una funzionalità avanzata utilizzata per consentire o impedire il traffico 
trasmesso attraverso il dispositivo. È possibile creare regole dettagliate per il dispositivo.
Per ulteriori dettagli ed esempi, fare riferimento al manuale.
