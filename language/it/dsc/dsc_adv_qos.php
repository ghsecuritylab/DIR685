<h1>Motore QoS (Quality of Service)</h1>
<p>
In questa sezione è possibile configurare la funzionalità QoS intelligente di D-Link. Tale funzionalità consente di ottimizzare 
la qualità dei dati voce VoIP o dello streaming impostando una priorità maggiore per il traffico VoIP o streaming rispetto al normale traffico di rete, 
ad esempio FTP o Web. Per prestazioni ottimali, selezionare l'opzione "Attesa eliminata" per impostare 
automaticamente la priorità per le applicazioni.
</p>
