<h1>Regole di filtro siti Web </h1>
<p>
I filtri dei siti Web rappresentano un utile strumento per limitare gli accessi a Internet. L'opzione URL del
sito Web consente di creare rapidamente un elenco di siti Web ai quali gli utenti possono o non possono accedere. 
L'opzione Pianificazione consente di controllare quando i client o i PC connessi 
al router sono autorizzati ad accedere a Internet.
</p>
