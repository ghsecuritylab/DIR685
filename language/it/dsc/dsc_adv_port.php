<h1>Regole di inoltro porte</h1>
L'opzione Inoltro porte consente di definire una singola porta pubblica sul router per il reindirizzamento 
a un indirizzo IP LAN interno e se necessario a una porta LAN privata. 
Questa funzionalità è utile per l'hosting di servizi online quali server FTP o Web.
