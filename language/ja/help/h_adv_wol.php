<strong>使用上のヒント</strong><br>
<br>
&nbsp;&#149;&nbsp; Wake on LAN機能を使用すると、ルータのLAN側に接続するPCの電源をリモートから投入できるようになります。ルータがWake on LANリクエストを有害な攻撃であるとみなして、ブロックしてしまわないように、Wake on LANマジックパケットを送信するUDPポートを指定します。<br>

