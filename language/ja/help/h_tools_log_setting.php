<strong>保存</strong>ボタンをクリックすると、ログファイルはローカルのハードディスクに保存され、
後でネットワーク管理者にトラブルシューティングのために送信できるようになります。また、<strong>ログタイプ</strong>から
ログ収集の対象となるイベントのタイプを指定できます。
