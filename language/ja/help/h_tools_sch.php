<strong>使用上のヒント</strong><br>
<br>
&nbsp;&#149;&nbsp; スケジュールは、さまざまな機能と連係して、それらの機能が有効になるタイミングを定義するものです。<br><br>
&nbsp;&#149;&nbsp; 各スケジュールに識別しやすい名前を与えます。例えば、月曜から木曜の午後3時から9時のスケジュールには、
&quot;放課後&quot;という名前を与えます。<br><br>
&nbsp;&#149;&nbsp;スケジュールを入力したら<strong>保存</strong> ボタンをクリックすると、スケジュールが登録されます。<br><br>
既存のスケジュールを変更するには、「編集」アイコンをクリックします。<br><br>
スケジュールを永久に削除するには、「削除」アイコンをクリックします。

