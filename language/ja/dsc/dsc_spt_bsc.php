<a name="lan_setup"><h2>ネットワーク設定</h2></a>
<table border=0 cellspacing=0 cellpadding=0>
<tr>
	<td height=20>
		<p>
			デバイスのLANインタフェースの設定です。"プライベート設定"と呼ばれる場合もあります。必要であればLAN IPアドレスを変更できます。LAN IPアドレスはお客様のネットワーク内のみで使用されるアドレスで、インターネット側からは見ることができません。デフォルトのIPアドレスは192.168.0.1、サブネットマスクは255.255.255.0です。
			<br><br>
			<strong><em>IPアドレス</em></strong> -
			<?query("/sys/modelname");?>のIPアドレスです。デフォルトは192.168.0.1です。<br>
			<strong><em>サブネットマスク</em></strong> -
			<?query("/sys/modelname");?>のサブネットマスクです。デフォルトは255.255.255.0です。<br>
			<strong><em>ローカルドメイン名</em></strong> (任意) - お客様のネットワークのローカルドメイン名を入力します。
		</p>
	</td>
</tr>
<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=20>
		<p>
			<strong><em>DNSリレー</em></strong> -
			DNSリレーを有効化すると、ルータ配下のDHCPクライアントは、DNSサーバとしてルータのLAN側インタフェースのIPアドレスを割り当てられます。ルータが受信するすべてのDNSリクエストは、契約するISPのDNSサーバに転送されます。DNSリレーが無効化されると、ルータ配下のすべてのDHCPクライアントはISPのDNSサーバに割り当てられます。
		</p>
	</td>
</tr>
<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=20>
		<p>
			<strong>DHCPサーバ</strong><br>
			DHCPはDynamic Host Control Protocolの略です。DHCPサーバはネットワーク上のデバイスからIPアドレス取得要求があると、アドレスの割り当てを行います。これらのデバイスには"IPアドレスを自動で取得する"ように設定しておく必要があります。<?query("/sys/modelname");?>では、デフォルトでDHCPサーバ機能が有効化されています。DHCPアドレスプールには、ネットワーク上のクライアントに自動的に割り当てるアドレスの範囲が指定されます。
		</p>
		<p>
			<strong>DHCPアドレス予約</strong><br>
			あるPCに対してルータが常に同じIPアドレスを割り当てるようにしたい場合は、そのPCの「コンピュータ名」、「IPアドレス」および「MACアドレス」を入力します。また、現在のDHCPクライアントのリストのドロップダウンメニューからPCを選択します。
		</p>
		<p>
			<strong><em>最小IPアドレス</em></strong> DHCPサーバが割り当てるIPアドレスの最小値<br>
			<strong><em>最大IPアドレス</em></strong> DHCPサーバが割り当てるIPアドレスの最大値<br>
			<strong><em>リース時間</em></strong> IPアドレスをリースする時間（分）
		</p>
		<p>
			「ダイナミックDHCPクライアントテーブル」には、ユニットに接続するダイナミックDHCPクライアントの情報が表示されます。テーブルでは、各クライアントコンピュータのホスト名、IPアドレス、MACアドレスおよびリースの有効期限が確認できます。
		</p>
	</td>
</tr>
