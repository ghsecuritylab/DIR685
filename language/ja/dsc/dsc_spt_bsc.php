<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=142>
		<p>
			<strong>ダイナミックIPアドレス</strong><br>
			IPアドレスがISP(インターネットサービスプロバイダ)により自動的に割り振られる場合は、このオプションを選択します。ケーブル回線のプロバイダは通常IPアドレスの自動割り当てを行います。
		</p>
		<p>
			<strong><em>ホスト名 </em></strong> (任意) -
			このフィールドへの入力は任意ですが、ISPによっては入力が必要な場合があります。デフォルトのホスト名はルータのモデル番号です。<br>
			<strong><em>MACアドレス </em></strong> (任意) -
			このフィールドへの入力は任意ですが、ISPによっては入力が必要な場合があります。デフォルトのMACアドレスは、<?query("/sys/modelname");?>上のWANインタフェースに定義されたMACアドレスです。「MACアドレスをコピーする」ボタンをクリックすると、デバイスの設定に使用するコンピュータのイーサネットカードのMACアドレスが自動的にコピーされます。ISPが必要としていない場合、このフィールドへの入力は不要です。
			<br>
			<strong><em>プライマリDNSアドレス </em></strong> -
			ISPにより提供されるプライマリDNS（Domain Name Service）サーバのIPアドレスを入力します。<br>
			<strong><em>セカンダリDNSアドレス </em></strong> (任意) -
			ISPによりセカンダリDNSサーバのIPアドレスが提供されている場合は入力します。<br>
			<strong><em>MTU </em></strong> -
			MTU（Maximum Transmission/Transfer Unit）はネットワークに送信可能なパケットの最大サイズです。MTUの値より大きなサイズのメッセージは小さいパケットに分割されます。デフォルト値は1500ですが、この値を変更すると、ルータのパフォーマンスに悪影響を与える場合があります。お客様が契約するISP、または弊社のテクニカルサポート担当者による指示がない場合は、この値は変更しないでください。
		</p>
	</td>
</tr>
<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=47>
		<p>
			<strong>PPPoE</strong><br>
			ISPによりPPPoE（Point to Point Protocol over Ethernet）の使用が必要とされている場合には、このオプションを選択します。DSLのプロバイダは通常このオプションを使用します。お客様のPPPoE接続で使用するIPアドレスを自動的に取得するには、「ダイナミックPPPoE」を選択します（通常のPPPoEではこの方式が使用されます）。PPPoE接続にスタティックIPアドレスを使用する場合は、「スタティックPPPoE」を選択します。
		</p>
		<p>
			<strong><em>ユーザ名 </em></strong> - お客様のPPPoEユーザ名を入力します。<br>
			<strong><em>パスワード </em></strong> - お客様のPPPoEパスワードを入力します。<br>
			<strong><em>サービス名 </em></strong> (任意) -
			ISPがサービス名を使用してPPPoE接続を行う場合は、このフィールドにサービス名を入力します。<br>
			<strong><em>IPアドレス </em></strong> -
			スタティックPPPoEを選択した場合のみ入力します。スタティックPPPoE接続用のIPアドレスを入力してください。<br>
			<strong><em>MACアドレス </em></strong> (任意) -
			ISPによってはこのフィールドへの入力が必要な場合があります。デフォルトのMACアドレスは、<?query("/sys/modelname");?>上のWANインタフェースに定義されたMACアドレスです。「MACアドレスをコピーする」ボタンをクリックすると、デバイスの設定に使用するコンピュータのイーサネットカードのMACアドレスが自動的にコピーされます。ISPが必要としていない場合、このフィールドへの入力は不要です。
			<br>
			<strong><em>プライマリDNSアドレス </em></strong> -
			ISPにより提供されるプライマリDNS（Domain Name System）サーバのIPアドレス。スタティックPPPoEを選択した場合のみ入力する必要があります。ダイナミックPPPoEが選択されている場合は、本アドレスはISPにより自動的に割り当てられるため、このフィールドはデフォルト値のままにしてください。
			<br>
			<strong><em>セカンダリDNSアドレス </em></strong> (任意) -
			ISPからセカンダリDNSサーバIPアドレスが提供されている場合は、その値を入力します。<br>
			<strong><em>最大アイドル時間 </em></strong> -
			デバイスがPPPoEセッションを切断するまでの最大の非アクティブ時間（分）。非アクティブな状態でインターネット接続が保持される最大時間を指定します。定義した時間以上、非アクティブ状態が続いた場合、PPPoEセッションは切断されます。このオプションはオンデマンド接続モードの場合のみ適用されます。
			<br>
			<strong><em>MTU </em></strong> -
			MTU（Maximum Transmission/Transfer Unit）はネットワークに送信可能なパケットの最大サイズです。MTUの値より大きなサイズのメッセージは小さいパケットに分割されます。デフォルト値は1492です。この値を変更すると、ルータのパフォーマンスに悪影響を与える場合があります。お客様が契約するISP、または弊社のテクニカルサポート担当者による指示がない場合は、この値は変更しないでください。
			<br>
			<strong><em>接続モード</em></strong> -
			ルータがPPPoEセッションを切断しないようにするには、「常にオン」を選択します。ルータのインターネット接続/切断の時間を制御する場合は「手動」を選択します。「オンデマンド接続」を選択すると、ネットワーク上のデバイスがインターネット上のリソースにアクセスする時のみルータが接続を確立します。
		</p>
