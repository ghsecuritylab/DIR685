		</p>
	</td>
</tr>
<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=20>
		<p>
			<strong>無線セキュリティモード</strong><br>
			無線ネットワーク上で送受信される情報の整合性を保つために無線ネットワークのセキュリティは重要です。<?query("/sys/modelname");?>はWEP、WPAのみ、WPA2のみ、WPA/WPA2（自動検出）の4種類の無線セキュリティを提供します。
		</p>
	</td>
</tr>
<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=20>
		<p>
			<strong>WEP </strong><br>
			WEP（Wired Equivalent Protocol）は、無線LAN（ＷＬＡＮ）用のセキュリティプロトコルです。WEPではWLAN上で送受信されるデータを暗号化することで安全性を提供します。<?query("/sys/modelname");?>は、64ビットと128ビットの2つのレベルのWEP暗号方式をサポートします。WEPはデフォルトでは無効化されています。WEP設定を変更して、既存の無線ネットワークに適合させたり、無線ネットワークをカスタマイズできます。
		</p>
		<p>
			<strong><em>認証</em></strong> -
			認証とは、<?query("/sys/modelname");?>が無線ネットワークへのアクセスを試みるネットワークデバイスの身元を確認するプロセスを意味します。本デバイスでWEPを使用する場合、2種類の認証方法が提供されます。<br> 
			<strong><em>オープンシステム</em></strong> -
			このオプションを選択すると、ネットワークへのアクセスに必要な暗号化キーの提示を求められる前に、すべての無線デバイスと<?query("/sys/modelname");?>の接続が可能になります。<br>
			<strong><em>共有キー</em></strong> -
			このオプションを選択すると、<?query("/sys/modelname");?>との通信を試みる無線デバイスは、<?query("/sys/modelname");?>との接続を許可される前にネットワークアクセス用の暗号化キーの提示を求められます。<br>
			<strong><em>WEP暗号</em></strong> -
			ネットワークで使用するWEP暗号化方式のレベルを選択します。64ビットと128ビットの2つのレベルがサポートされています。<br>
			<strong><em>キータイプ</em></strong> -
			<?query("/sys/modelname");?>がサポートするキータイプはHEX（16進数）およびASCIIです。キータイプを変更して、既存の無線ネットワークに適合させたり、無線ネットワークをカスタマイズできます。<br>
			<strong><em>キー</em></strong> -
			キー1からキー4を指定して、ネットワーク保護に使用する無線暗号キーを簡単に変更できます。ネットワーク上の無線データの暗号化に使用するキーを1～4から選択します。
		</p>
	</td>
</tr>
<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=20>
		<p>
			<strong>WPA/WPA2</strong><br>
			Wi-Fi Protected Access (または Wi-Fi Protected Access 2) は、無線ネットワーク上でのユーザの認証を行います。WPAでは、キーが一定の間隔で定期的に変更されるため、WEPより高い安全性を提供します。
		</p>
		<p>
			<strong><em>暗号化タイプ</em></strong> -
			<?query("/sys/modelname");?>では、セキュリティタイプにWPAが指定される場合、TKIP（Temporal Key Integrity Protocol）とAES（Advanced Encryption Standard）の2種類の暗号タイプを提供します。<br>
			<strong><em>PSK/EAP</em></strong> -
			PSKを選択すると、認証を行うために無線クライアントからのパスフレーズの提示が必要となります。EAPを選択する場合は、すべての無線クライアントの認証を行うRADIUSサーバをネットワーク上に設置する必要があります。<br>
			<strong><em>パスフレーズ</em></strong> -
			PSKを使用する場合、無線クライアントが<?query("/sys/modelname");?>と通信する際に必要となります。8～63文字の英数字を入力してください。このパスフレーズは、ネットワークに無線デバイスを追加する際に必要となるため、書き留めるなどして保管してください。<br>
			<strong><em>802.1X</em></strong> -
			WPA認証が、ネットワーク上に設置したRADIUSサーバを使用して行われることを意味します。RADIUSサーバに設定したIPアドレス、ポート、共有秘密キーを入力してください。無線クライアントの認証に使用するRADIUSサーバがネットワーク上に2台ある場合は、2台目のRADIUSサーバの情報も入力してください。
		</p>
	</td>
</tr>
</table>
</div>


