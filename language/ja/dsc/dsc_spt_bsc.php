			<strong>手動によるインターネット接続セットアップ</strong><br>
			ルータをインターネットに接続するために必要な設定を手動で行う場合には、このオプションを選択します。
			<br>
			<strong>アクセスポイントモード</strong><br>
			「アクセスポイントモード」を有効化すると、デバイスはワイヤレスAPとして機能するようになります。無線接続関連の設定を除く全てNAT機能は無効化されます。
