	<td height=20>
		<p>
			<strong>WPS（Wi-Fi Protected Setup：Windows Vistaの場合はWCN 2.0）</strong><br>
			本機能により、無線のセキュリティ設定がさらに直感的に行えるようになります。この機能はPIN（個人暗証番号）とプッシュボタンの2つの方式で実現できます。デバイスの無線LANカードユーティリティに付属のPINを入力することで、ルータとクライアント間の無線セキュリティの設定を自動的に行えます。無線LANカードにWindows Vista対応ドライバが含まれる場合はWindows Vistaのワイヤレスクライアントユーティリティに入力します。この機能を利用するには、無線LANカードがWPSのいずれかの方式をサポートしている必要があります。
		</p>
	</td>
</tr>
<tr><td height=20>&nbsp;</td></tr>
<tr>
	<td height=20>
