<tr>
	<td height=61>
		<p>
			<strong>スタティックIPアドレス</strong><br>
			IPアドレス、サブネットマスク、デフォルトゲートウェイ、およびDNSサーバアドレスの情報がお客様が契約するISP(インターネットサービスプロバイダ)により提供される場合、このオプションを選択します。これらの情報をそれぞれのフィールドに入力してください。フィールドに入力する値が不明な場合は、ご契約のISPにお問い合わせください。
		</p>
	</td>
