<h1>Portweiterleitungsregeln</h1>
Mithilfe der Option Portweiterleitung können Sie nach Bedarf einen einzelnen öffentlichen Port auf Ihrem Router zur 
Weiterleitung an eine interne LAN IP-Adresse festlegen sowie einen anderen Port für den LAN-Server einstellen.
Diese Funktion dient dazu, Online-Dienste wie FTP oder Webserver als Host bereitzustellen.
