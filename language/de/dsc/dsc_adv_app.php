<h1>Anwendungsregeln</h1>
Die Option Anwendungsregeln wird verwendet, um einzelne oder mehrere Ports in Ihrer Firewall zu öffnen, wenn der Router Daten erfasst, 
die von einem ausgehenden Trigger-Port oder einem Portbereich ins Internet gesendet wurden.
Sonderanwendungsregeln werden für alle Computer in Ihrem internen Netzwerk angewendet.
