<h1>Website-Filterregeln </h1>
<p>
Website-Filter sind nützliche Tools zur Einschränkung des Internetzugangs. Die Option Website URL (Website-URL) 
ermöglicht Ihnen die schnelle Erstellung von Listen aller Websites, auf die die Benutzer zugreifen oder nicht zugreifen dürfen. 
Mithilfe der Option Zeitplan können Sie außerdem steuern, wann mit dem Router 
verbundenen Clients oder PCs der Internetzugang ermöglicht wird.
</p>
