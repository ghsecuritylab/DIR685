Geben Sie die Zeit entweder manuell ein, indem Sie auf 
<strong>Sync. Your Computers Time Settings</strong> (Zeiteinstellungen Ihres Computers synchronisieren) klicken, 
oder verwenden Sie die Option <strong>Automatic Time Configuration</strong> (Automatische Zeitkonfiguration), 
damit Ihr Router die Zeit mit einem Zeitserver im Internet synchronisiert.<br>

