<strong>Hilfreiche Tipps...</strong><br>
<br>
&nbsp;&#149;&nbsp; Zeitpläne werden mit einer Reihe von anderen Funktionen verwendet, um mit ihrer Hilfe festzulegen, wann diese Funktionen wirksam sein sollen.<br><br>
&nbsp;&#149;&nbsp; Geben Sie jedem Zeitplan einen für Sie aussagefähigen Namen. So könnte beispielsweise ein Zeitplan für Montag bis Freitag 
von 15:00 Uhr bis 21:00 Uhr &quot;Nach der Schule&quot; genannt werden.<br><br>
&nbsp;&#149;&nbsp; Klicken Sie auf <strong>Speichern</strong>, um der Liste unten einen fertig gestellten Zeitplan hinzuzufügen.<br><br>
&nbsp;&#149;&nbsp; Klicken Sie auf das Symbol <strong>Bearbeiten</strong>, wenn Sie einen bereits fertig gestellten Zeitplan ändern möchten.<br><br>
&nbsp;&#149;&nbsp; Klicken Sie auf das Symbol <strong>Löschen</strong>, um einen Zeitplan zu löschen.

