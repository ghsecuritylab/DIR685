<strong>Hilfreiche Tipps...</strong>
<br>
<p>&nbsp;&#149;&nbsp;Aktivieren:<br>
Gibt an, ob der Eintrag aktiviert oder deaktiviert wird.</p>

<p>&nbsp;&#149;&nbsp;Schnittstelle:<br>
Gibt die Schnittstelle an -- WAN oder WAN Physisch --, die das IP-Paket verwenden muss, 
um bei Verwendung dieser Route den Datenverkehr aus dem Router zu leiten.</p>

<p>&nbsp;&#149;&nbsp;Destination IP: (Ziel-IP)<br>
Die IP-Adresse der Pakete, die diese Route nehmen werden.</p>

<p>&nbsp;&#149;&nbsp;Subnetzmaske:<br>
Ein Bit in der Maske gibt an, welche Bits der IP-Adresse entsprechen müssen.</p>

<p>&nbsp;&#149;&nbsp;Gateway:<br>
Gibt den nächsten Hop (Übergang von einem Netzknoten zum nächsten) an, wenn diese Route verwendet wird. Ein Gateway 
0.0.0.0 kennzeichnet, dass es kein nächster Hop existiert und die entsprechende IP-Adresse direkt mit dem 
Router auf der angegebenen Schnittstelle verbunden ist: WAN oder WAN Physisch.</p>

